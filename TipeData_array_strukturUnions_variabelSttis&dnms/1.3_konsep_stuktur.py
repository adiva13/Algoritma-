#Struktur adalah kumpulan beberapa data yang berbeda-beda tipe, tetapi digabung menjadi satu kesatuan. Struktur digunakan ketika kita ingin menyimpan data yang berhubungan dalam satu paket.

#data_kendaraan
kendaraan = {
    "merk": "Yamaha",
    "tahun": 2020,
    "warna": "Hitam"
}

print("Merk:", kendaraan["merk"])
print("Tahun:", kendaraan["tahun"])
print("Warna:", kendaraan["warna"])

#Struktur ini menyimpan data kendaraan dalam satu wadah.
