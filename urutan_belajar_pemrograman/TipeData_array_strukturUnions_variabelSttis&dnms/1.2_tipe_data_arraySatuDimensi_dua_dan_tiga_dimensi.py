#Array_satu_dimensi
#Array 1 dimensi adalah kumpulan data yang disusun dalam satu baris. Bisa dilihat seperti pada deretan list biasa.

angka = [10, 20, 30]

print("Array 1 Dimensi:", angka)

#Array_dua_dimensi
#Array 2 dimensi berbentuk baris dan kolom, seperti tabel / matriks.

matriks = [
    [1, 2, 3],
    [4, 5, 6]
]

print("Array 2 Dimensi:")
print(matriks)

#Array_tiga_dimensi
#Array 3 dimensi adalah array yang berisi array 2 dimensi di dalamnya. Seperti tumpukan beberapa matriks.

array3D = [
    [
        [1, 2],
        [3, 4]
    ],
    [
        [5, 6],
        [7, 8]
    ]
]

print("Array 3 Dimensi:")
print(array3D)
