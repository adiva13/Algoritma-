#tipe data character

karakter = 'D'
print("Nilai karakter:", karakter)

#Variabel karakter menyimpan satu huruf yaitu 'D'. Karena hanya satu karakter, tipe datanya disebut karakter (char).

#tipe data integer
angka_bulat = 100
print("Nilai integer:", angka_bulat)

#Variabel angka_bulat berisi nilai 100,  bilangan bulat tanpa koma. Angka seperti ini disebut integer (int).

#tipe data floating point
angka_desimal = 4.75
print("Nilai floating point:", angka_desimal)

#Variabel angka_desimal berisi 4.75, yaitu angka yang punya koma/desimal. Ini disebut tipe floating point.