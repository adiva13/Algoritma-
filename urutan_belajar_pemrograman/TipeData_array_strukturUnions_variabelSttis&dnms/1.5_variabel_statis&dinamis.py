#Variabel statis adalah variabel yang nilai atau ruang memorinya tetap selama program berjalan. Variabel ini tidak berubah tempatnya dan juga biasanya sudah ditentukan sejak awal.

#contoh variabel statis
x = 90
print("Nilai variabel statis:", x)

#Variabel dinamis adalah variabel yang nilainya bisa berubah-ubah, bahkan bisa berubah tipe datanya saat program berjalan.

#contoh variabel dinamis
y = 40
print("Nilai awal variabel dinamis:", y)

y = "Halo"
print("Nilai setelah berubah:", y)

